#include <stdio.h>

int main() {
    int i, j;
    char diamond[7][7] = {
        {' ',' ',' ','*',' ',' ',' '},
        {' ',' ','*','*','*',' ',' '},
        {' ','*','*','*','*','*',' '},
        {'*','*','*','*','*','*','*'},
        {' ','*','*','*','*','*',' '},
        {' ',' ','*','*','*',' ',' '},
        {' ',' ',' ','*',' ',' ',' '},
    };

    for(i=0;i<=6;i++) {
        for(j = 0; j <= 6; j++) {
            printf("%c", diamond[i][j]);
        }
        printf("\n");
    }
    return 0;
}

